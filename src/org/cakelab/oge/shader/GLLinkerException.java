package org.cakelab.oge.shader;

@SuppressWarnings("serial")
public class GLLinkerException extends GLException {

	public GLLinkerException() {
		super();

	}

	public GLLinkerException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public GLLinkerException(String message, Throwable cause) {
		super(message, cause);

	}

	public GLLinkerException(String message) {
		super(message);

	}

	public GLLinkerException(Throwable cause) {
		super(cause);

	}

}
